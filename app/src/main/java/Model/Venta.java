package Model;

public class Venta {
    public int idproducto;
    public int idventa;
    public int total;
    public String estado;

    public int getIdproducto(){
        return idproducto;
    }
    public int getIdventa(){
        return idventa;
    }
    public int getTotal(){
        return total;
    }
    public String getEstado(){
        return estado;
    }
}
