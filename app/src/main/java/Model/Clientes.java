package Model;

public class Clientes {
    public int id;
    public String nombre;
    public String apellido;
    public String cedula;
    public String email;
    public String clave;

    public int getId(){
        return id;
    }
    public String getNombre(){
        return nombre;
    }
    public String getApellido(){
        return apellido;
    }
    public String getCedula(){
        return cedula;
    }
    public String getClave(){
        return clave;
    }
}
